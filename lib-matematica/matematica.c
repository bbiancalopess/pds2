#include <stdio.h>
#include "matematica.h"
#include <math.h>

float calcular_seno(float angulo_radiano){
    return(sin(angulo_radiano));
}

float calcular_cosseno(float angulo_radiano){
    return(cos(angulo_radiano));
}

float calcular_tangente(float angulo_radiano){
    return(tan(angulo_radiano));
}